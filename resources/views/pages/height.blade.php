                    <h2>Как обучать работе на высоте с учетом требований Порядка № 2464</h2>
                    <p>Теперь можно совмещать обучение работе на высоте и обучение работам повышенной
                        опасности. Возьмите в работу единую программу обучения работе на высоте. Она
                        учитывает требования Порядка № 2464 и ПОТ № 782н. По алгоритму Системы обучите
                        работам на высоте в два раза быстрее.
                    </p>
                    <h2>
                        По каким правилам
                    </h2>
                    <p>Обучайте работам на высоте по двум НПА:</p>
                    <ul>
                        <li>
                            Правилам по охране труда при работе на высоте, утвержденным приказом Минтруда
                            от 16.11.2020 № 782н (далее — ПОТ № 782н);
                        </li>
                        <li>
                            Порядку обучения по охране труда и проверки знания требований охраны труда,
                            утвержденному постановлением Правительства от 24.12.2021 № 2464 (далее — Порядок
                            № 2464).
                        </li>
                    </ul>
                    <p>
                        Обучение безопасным методам и приемам выполнения работ на высоте по ПОТ
                        № 782н относится к обучению требованиям охраны труда по программе В.
                        Работы на высоте — это работы повышенной опасности. Для работ повышенной
                        опасности применяют отраслевые правила по охране труда. Правила по охране труда при
                        работе на высоте установлены ПОТ № 782н. Дополнительные требования к организации
                        обучения охране труда приведены в разделе 2 ПОТ № 782н.
                    </p>
                    <p>
                        Работодатель или уполномоченный работник обязан организовать до начала
                        проведения работы на высоте обучение безопасным методам и приемам выполнения
                        работ (п. 16 ПОТ № 782н). Работников, которые отвечают за организацию работ
                        повышенной опасности и сами выполняют такие работы, обучают по программе
                        В (п. 55 Порядка № 2464).
                    </p>
                    <p>
                        Примерные перечни тем для формирования программ обучения требованиям
                        охраны труда представлены в приложении 3 к Порядку № 2464. Для программ В не
                        сформированы темы, потому что знания и навыки, которые должен приобрести работник
                        в период обучения, прописаны в ПОТ. Чему должен научиться работник
                        с соответствующей группой по высоте, определили в пунктах 17–21 ПОТ № 782н.
                        При обучении работе на высоте по программе В соблюдайте дополнительные
                        требования из ПОТ № 782н, например, после экзамена выдайте удостоверение и оформите
                        протокол по результатам периодической проверки знаний.
                        Не обучайте работников отдельно работе на высоте по ПОТ
                        № 782н и по программе В, чтобы не дублировать обучение. Программы будут схожи,
                        а работник не получит новых знаний.
                    </p>
                    <p>
                        Эксперты Системы направляли обращения в Минтруд, чтобы уточнить, можно ли
                        совмещать обучение работе на высоте и обучение работам повышенной опасности.
                        Редакция получила ответы 5 мая. Министерство подтвердило, что обучение работе на
                        высоте относится к обучению требованиям охраны труда по программе В.
                        Поэтому сведения об обучении безопасным методам и приемам выполнения работ
                        на высоте вносите в реестр обученных лиц. Такого мнения придерживается Минтруд
                        в письмах № 15-2/ООГ-2218, 15-2/ООГ-2247, 15-2/ООГ-2220, 15-2/ООГ-2215, 15-2/ООГ-
                        2198.
                    </p>
                    <h2>
                        Кто может выполнять работы на высоте
                    </h2>
                    <p>Любой работник, выполняющий работы на высоте, должен:</p>
                    <ul>
                        <li>
                            получить квалификацию, соответствующую характеру выполняемых работ;
                        </li>
                        <li>пройти обучение безопасным методам и приемам выполнения работ на высоте.</li>
                    </ul>
                    <p>
                        Квалификацию получают до трудоустройства и выполнения должностных
                        обязанностей. Она подтверждает обучение на конкретную профессию и позволяет
                        работнику выполнять определенные трудовые функции, например слесарю строительному
                        6-го разряда проводить ремонт особо сложного и тяжелого оборудования
                        (ч. 11 ст. 60 Закона от 29.12.2012 № 273-ФЗ). Уровень квалификации определяет документ
                        о профессиональном образовании или о квалификации (п. 13 ПОТ № 782н). Его выдают
                        обучающие организации с лицензией на образовательную деятельность.
                    </p>
                    <p>
                        Квалификации работнику недостаточно, чтобы выполнять работы на высоте.
                        Работник должен пройти обучение охране труда. Обучение безопасным методам
                        и приемам выполнения работ на высоте не имеет отношения к получению профессии.
                        Высота — это условие работы, при котором работник трудится по профилю своей
                        деятельности. Обучение работе на высоте — это обучение охране труда, которое проводит
                        работодатель после приема на работу и до выполнения работ на высоте. Такое обучение
                        оформляют протоколом, присваивают группу, выдают удостоверение и проводят
                        периодическую проверку знания.
                    </p>
                    <h2>
                        Какую группу присвоить
                    </h2>
                    <p>
                        Работникам с минимальным возможным риском падения с высоты не присваивают
                        группу по безопасности. Таких работников допускают к выполнению работ на высоте без
                        оформления наряда-допуска (п. 8 ПОТ № 782н).
                    </p>
                    <p>
                        Работники, которых допускают к выполнению работ на высоте с оформлением
                        наряда-допуска, делятся на три группы по безопасности работ на высоте. Группа зависит
                        от категории работников и функций, которые они выполняют (п. 14, 15, 23 ПОТ № 782н).
                        Какую группу должны получить работники для работы на высоте, смотрите в таблице.
                    </p>

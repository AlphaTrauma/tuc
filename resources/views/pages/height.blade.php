                    <h2>
                        Какие виды обучения
                    </h2>
                    <p>
                        Для всех сотрудников, которые работают на высоте,
                        организуйте первичное и периодическое обучение в учебных центрах или в комиссии
                        работодателя. После обучения проводите стажировку. Далее проверяйте
                        знания работников с установленной периодичностью.
                    </p>
                    <h2>
                        Первичное обучение
                    </h2>
                    <p>До начала проведения работы организуйте первичное обучение безопасным
                        методам и приемам выполнения работ на высоте работникам (п. 16 ПОТ № 782н):</p>
                    <ul>
                        <li>впервые допускаемым к работе на высоте;</li>
                        <li>при переводе с других работ, если работники не проходили обучение;</li>
                        <li>имеющим перерыв в работе на высоте более одного года.</li>
                    </ul>
                    <hr>
                    <blockquote>
                        <p class="uk-margin-small-bottom">
                            <strong>
                                ВНИМАНИЕ<br>
                                Обучать работе на высоте можно только совершеннолетних работников
                            </strong>
                        </p>
                        <footer>Это следует из пункта 12 ПОТ № 782н</footer>
                    </blockquote>
                    <hr>
                    <p>
                        Проведите обучение в течение 60 календарных дней после приема на работу или
                        перевода на другую работу (п. 62 Порядка № 2464). При этом работодатель не может
                        допустить к самостоятельной работе до тех пор, пока не проведет обучение и проверку
                        знаний (абз. 3 ч. 1 ст. 76 ТК). Поэтому чем быстрее обучите работника, тем лучше.
                    </p>
                    <h2>
                        Периодическое обучение
                    </h2>
                    <p>
                        Периодичность обучения по программе В установлена НПА. Если в документах
                        отсутствуют требования, работников обучают не реже одного раза в год (п. 60 Порядка №
                        2464). В ПОТ № 782н указана периодичность для работников с группой, поэтому их не
                        надо обучать работе на высоте каждый год.
                    </p>
                    <p>
                        Периодическое обучение организуйте раз в три года для работников
                        с 1 и 2 группами по безопасности и не реже одного раза в пять лет – для работников
                        с 3 группой (п. 23 ПОТ № 782н).
                    </p>
                    <p>
                        Необходимость периодического обучения безгруппников определяет работодатель
                        (п. 22 ПОТ № 782н). Не организовывайте отдельное периодическое обучение
                        безопасности работ на высоте для безгруппников, а включите необходимые темы
                        в программу Б. Обучайте таких работников не реже одного раза в три года (п. 59 Порядка
                        № 2464).
                    </p>

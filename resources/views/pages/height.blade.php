                    <h3>
                        Разработайте программы стажировок
                    </h3>
                    <p>
                        В программах учитывайте требования Порядка № 2464 и ПОТ
                        № 782н. Продолжительность стажировки работодатель определяет самостоятельно в ЛНА,
                        минимальная продолжительность — не менее двух смен (п. 30 ПОТ
                        № 782н, п. 28 Порядка № 2464).
                    </p>
                    <h3>
                        Издайте приказ об обучении
                    </h3>
                    <p>
                        Перед обучением работников издайте приказ. В документе укажите сроки
                        проведения обучения, Ф.И.О. и должности обучаемых работников. Обучение обязательно
                        проходит с отрывом от производства (п. 65 Порядка № 2464). Сохраните за работником
                        место работы и средний заработок (ст. 187 ТК).
                    </p>
                    <h3>
                        Как проходит обучение
                    </h3>
                    <p>
                        Процесс обучения работам на высоте включает само обучение работников, экзамен,
                        стажировку на конкретных рабочих местах и периодическую проверку знания.


                    </p>
                    <h3>
                        Первичное и периодическое обучение

                    </h3>
                    <p>
                        Обучение состоит из теоретической и практической частей. Для теоретической части
                        можно применять дистанционный формат обучения. Практическую часть обучения
                        проводят на специально оборудованных стендах или полигонах (п. 17 ПОТ № 782н).
                    </p>
                    <hr>
                    <blockquote>
                        <p><strong>ВНИМАНИЕ<br>
                                Запрещено обучать работников заочно и только дистанционным способом</strong>
                            Работники должны знать и уметь применять безопасные методы и приемы
                            выполнения работ на высоте, а также обладать практическими навыками. </p>
                        <footer>Это следует
                            из пункта 17 ПОТ № 782н и пункта 49 Порядка № 2464</footer>
                    </blockquote>
                    <hr>
                    <p>
                        Обучение проводят по программам, которые учитывают требования Порядка
                        № 2464 и ПОТ № 782н.
                    </p>
                    <h3>
                        Экзамен
                    </h3>
                    <p>
                        После обучения работники сдают экзамен (п. 24 ПОТ № 782н). Работодатель вправе
                        совместить проведение проверки знаний с экзаменом, который проводят после
                        периодического обучения на 1 и 2 группы (п. 32 ПОТ № 782н).
                    </p>
                    <p>
                        На каждом предприятии самостоятельно принимают решение, как проверить знания
                        работников: в устной форме, в виде тестирования или иным способом. По окончании
                        экзамена работникам выдают удостоверения, личные книжки и протоколы. Как оформить
                        результаты обучения и проверки знаний при работе на высоте, читайте ниже.
                    </p>
                    <h3>
                        Стажировка
                    </h3>
                    <p>Перед допуском к работе организуйте стажировку работников. Во время стажировки
                        сотрудник отрабатывает практические навыки выполнения работ с использованием
                        знаний и умений, которые получил в рамках обучения требованиям по охране труда
                        (п. 28 ПОТ № 782н, п. 25 Порядка обучения № 2464).
                        </p>
                    <p>Продолжительность стажировки работодатель определяет самостоятельно в ЛНА,
                        минимальная продолжительность — не менее двух смен (п. 30 ПОТ №
                        782н, п. 28 Порядка № 2464).
                        </p>
                    <p>Для отдельных категорий работников 3 группы работодатель сам определяет
                        продолжительность, содержание и назначение руководителя стажировки (п. 29 ПОТ
                        № 782н).
                        </p>
                    <p>После успешного прохождения стажировки работника допускают
                        к самостоятельной работе. Как документально оформить стажировку и допуск к работе.</p>
                    <h3>
                        Периодическая проверка знаний
                    </h3>
                    <p>Работодатель вправе совместить проверку знаний с экзаменом, который проводят
                        после периодического обучения на 1 и 2 группы.
                        </p>
                    <p>
                        Методика проведения периодической проверки знаний не регламентирована ПОТ
                        № 782н. Работодатель сам выбирает формат проведения проверки знаний. Подготовьте
                        билеты, тесты в бумажном или электронном виде. Включите задания с построением
                        отдельных систем безопасности при работе на высоте. Требования к применению систем
                        обеспечения безопасности при работе на высоте указаны в разделе 6 ПОТ № 782н.
                    </p>
                    <h3>
                        Как оформить результаты обучения и проверки знаний
                    </h3>
                    <p>
                        Обучение охране труда при работе на высоте заканчивается экзаменом. Его вправе
                        проводить комиссия, в которую включены преподаватели и специалисты с 3 группой
                        безопасности (п. 24 ПОТ № 782н).
                    </p>
                    <p>
                        В зависимости от присвоенной группы по результатам обучения
                        работникам выдают:
                    </p>
                    <ul>
                        <li>удостоверение о допуске к работам на высоте для работников без группы;</li>
                        <li>удостоверение о допуске к работам на высоте для работников с присвоенной группой;</li>
                        <li>личную книжку учета работ на высоте</li>
                    </ul>
                    <h3>
                        Удостоверение для работников без группы
                    </h3>
                    <p>
                        Работникам, которым не присвоена группа безопасности,
                        выдают удостоверение по форме, утвержденной приложением № 1 к ПОТ № 782.
                    </p>
                    <p>
                        Удостоверение остается действительным до окончания срока его действия, если
                        (п. 25 ПОТ № 782н):
                    </p>
                    <ul>
                        <li>изменилась фамилия работника;</li>
                        <li>переименовали организацию и условия труда работника не изменили;</li>
                        <li>работника перевели на другую должность, но его обязанности по организации
                            и выполнению работ на высоте остаются прежними и не требуют дополнительных знаний.</li>
                    </ul>
                    <h3>
                        Удостоверение для работников с присвоенной группой
                    </h3>
                    <p>
                        Работникам, которым присвоена одна из групп безопасности, выдают удостоверение
                        по форме, установленной приложением № 3 к ПОТ № 782н.
                    </p>
                    <p>
                        Удостоверение остается действительным до окончания срока его действия, если
                        (п. 26 ПОТ № 782н):
                    </p>
                    <ul>
                        <li>
                            изменилась фамилия работника;
                        </li>
                        <li>переименовали организацию и условия труда работника не изменили;</li>
                        <li>
                            работника перевели на другую должность, но его обязанности по организации и выполнению работ на высоте остаются прежними и не требуют дополнительных знаний.
                        </li>
                    </ul>
                    <h3>
                        Личная книжка учета работ на высоте
                    </h3>
                    <p>
                        Работникам, которые выполняют работы на высоте с применением систем канатного
                        доступа, дополнительно к удостоверению выдают личную книжку учета работ на высоте
                        (п. 26 ПОТ № 782н). Образец приведен в приложении № 4 к ПОТ № 782н.
                    </p>
                    <p>
                        Личная книжка удостоверяет (п. 27 ПОТ № 782н):
                    </p>
                    <ul>
                        <li>количество отработанных часов при работе на высоте;</li>
                        <li>время, потраченное на подготовку оборудования и средств защиты, обследование
                            и испытание оборудования, обследование и подготовку рабочего места;
                        </li>
                        <li>сведения о максимальной высоте, на которой проводилась работа, и наименование
                            высотного объекта.</li>
                    </ul>
                    <h3>
                        Протокол
                    </h3>
                    <p>

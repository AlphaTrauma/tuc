                    <h2>
                        Стажировка
                    </h2>
                    <p>
                        Организуйте стажировку после того, как работник прошел обучение и получил
                        удостоверение. Стажировку на рабочем месте проводят с работниками, которые
                        выполняют работы повышенной опасности. Это следует из пункта 26 Порядка № 2464.
                        Работники без группы также проходят стажировку (п. 28 ПОТ № 782н).
                    </p>
                    <h2>
                        Периодическая проверка знания
                    </h2>
                    <p>
                        Периодическую проверку знаний проводят, чтобы работники подтвердили свой
                        уровень знаний и навыки, необходимые для безопасного выполнения работ на высоте.
                        Проверка знаний проходит без обучения.
                    </p>
                    <p>
                        Ежегодно проводите периодическую проверку знания для работников без группы и
                        для работников 1 и 2 групп безопасности. Для работников 3 группы работодатель
                        определяет необходимость проверки знаний и прописывает требования в документах
                        СУОТ (п. 33 ПОТ № 782н).
                    </p>
                    <p>
                        По окончании периодической проверки знаний оформляют протокол.
                        Работодатель вправе совместить проверку знаний с экзаменом, который проводят после
                        периодического обучения на 1 и 2 группы (п. 32 ПОТ № 782н).
                    </p>
                    <h2>
                        Как определить, где обучать: в учебном центре или внутри организации
                    </h2>
                    <p>
                        Каких работников обучать работе на высоте в учебном центре, зависит от категории
                        работников и их трудовых функций. Обучение по программе В в учебном центре
                        проходят:
                    </p>
                    <ul>
                        <li>ответственные за организацию, выполнение и контроль работ повышенной опасности;</li>
                        <li>члены комиссии по проверке знания требований ОТ работников, выполняющих работы
                            повышенной опасности;</li>
                        <li>ответственные за инструктажи по ОТ и обучение требованиям ОТ работников,
                            выполняющих работы повышенной опасности.</li>
                    </ul>
                    <p>Работники 3 группы безопасности являются ответственными за организацию
                        и безопасное проведение работ на высоте, проводят обучение и входят в состав
                        экзаменационной комиссии (п. 21 ПОТ № 782н). Направьте их на обучение в учебный
                        центр.</p>
                    <p>Работников 2 группы безопасности также обучите в учебном центре. Они проводят
                        инструктажи по охране труда работникам, которые выполняют работы повышенной
                        опасности.</p>
                    <p>Работодатель самостоятельно выбирает, где обучать остальных работников:
                        в организации или в специализированном учебном центре. Для этого оценивает свои
                        временные, кадровые и денежные ресурсы.</p>
                    <p>Если работодатель планирует обучать высоте внутри организации, то ему нужно:</p>
                    <ul>
                        <li>сформировать комиссии и назначить ответственных;</li>
                        <li>оборудовать полигоны или стенды для занятий;</li>
                        <li>закупать учебные материалы;</li>
                        <li>составлять программы обучения;</li>
                        <li>вносить данные в реестр обученных лиц по охране труда.</li>
                    </ul>
                    <h2>
                        Как обучить в учебном центре
                    </h2>
                    <p>
                        Воспользуйтесь пошаговым алгоритмом, чтобы направить работников на обучение
                        работам на высоте в учебный центр.
                    </p>
                    <p>
                        <b>Шаг 1.</b> Найдите обучающую организацию. <br>
                        Перед тем как заключить договор с УЦ, проверьте наличие аккредитации
                        на обучение охране труда на сайте Минтруда. Правила аккредитации
                        утверждены постановлением Правительства от 16.12.2021 № 2334. <br>
                        Программа должна включать теоретическую и практическую части. Теоретическая
                        часть может быть дистанционной, но практика должна быть очной (п. 36 Порядка
                        № 2464). Практическую часть обучения безопасным методам и приемам выполнения
                        работ на высоте проводят на специально оборудованных стендах или полигонах.
                        Преподаватели в учебном центре должны быть старше 21 года и иметь опыт
                        выполнения работ на высоте более 2 лет (п. 21 ПОТ № 782н).
                    </p>
                    <p>
                        <b>Шаг 2.</b> Заключите договор с учебным центром. <br>
                        Как правило, учебный центр использует собственный типовой договор на оказание
                        услуг при обучении охране труда при работе на высоте. Согласуйте сроки, формат
                        обучения и количество слушателей.
                    </p>
                    <p><b>Шаг 3.</b> Издайте приказ о направлении работника на обучение в учебный центр.<br>
                        В приказе укажите Ф. И. О. и должности работников. При необходимости также
                        укажите дополнительную информацию, например, даты обучения и учебный центр,
                        в который направляете сотрудников. Ознакомьте работников с приказом.
                    </p>
                    <p>
                        <b>Шаг 4.</b> Направьте работников на обучение в указанные сроки. <br>
                        Обучение обязательно проходит с отрывом от производства (п. 65 Порядка
                        № 2464). Сохраните за работником место работы и средний заработок (ст. 187 ТК).
                    </p>
                    <hr>
                    <blockquote>
                        <p>
                            <strong>
                                ВНИМАНИЕ<br>
                                Нельзя брать деньги у работников за обучение<br>
                            </strong>
                            Обязательное обучение работников охране труда в учебном центре организуют за счет
                            работодателя.
                        </p>
                        <footer>
                            Это следует из абзаца 7 части 1 статьи 216 Трудового кодекса.
                        </footer>
                    </blockquote>
                    <hr>
                    <p>
                        <b>Шаг 5.</b> Проверьте документы, которые получили работники.<b></b>
                        Проверьте, что работники получили по результатам обучения удостоверение
                        о допуске к работам на высоте, а сотрудники, которые работают с системами канатного
                        доступа, — дополнительно к удостоверению личную книжку учета работ на высоте.
                        По запросу работодателя учебный центр выдаст выписку из протокола проверки
                        знания. Сведения об обучении в реестр обученных охране труда лиц вносит учебный
                        центр самостоятельно. Также обучение вносят в реестр, если совместили проверку знаний
                        с экзаменом, который проводят после периодического обучения на 1 и 2 группы. Это
                        следует из письма Минтруда от 05.05.2023 № 15-2/ООГ-2247.

                    </p>
                    <p>
                        <b>Шаг 6.</b> Проведите стажировку и допустите работника до работ.<b></b>
                        Чтобы допустить сотрудника к стажировке, руководитель организации или
                        структурного подразделения издает приказ. В приказе указывают календарные сроки
                        стажировки и фамилии ответственных лиц. Продолжительность стажировки
                        устанавливают индивидуально в зависимости от уровня профессионального образования,
                        опыта работы и профессии обучаемого. Минимальная продолжительность — две смены
                        (п. 30 ПОТ № 782н, п. 32 Порядка обучения № 2464).
                    </p>
                    <p>
                        После успешного прохождения стажировки сотрудника допускают
                        к самостоятельной работе.
                    </p>
                    <h2>
                        Как организовать обучение на территории работодателя
                    </h2>
                    <p>
                        Перед обучением оборудуйте стенд или полигон, назначьте ответственных,
                        сформируйте комиссию и составьте программы. Обучение работников проводите
                        с отрывом от производства. После обучения оформите документы и внесите сведения
                        в реестр обученных охране труда лиц.
                    </p>
                    <h3>
                        Как подготовиться к обучению
                    </h3>
                    <p>Организуйте подготовку к обучению работе на высоте по чек-листу Системы.</p>
                    <h3>
                        Оборудуйте стенд или полигон
                    </h3>
                    <p>Конкретных требований к обустройству стендов и полигонов в законе нет. Выбор
                        оборудования зависит от видов работ, которые будет выполнять персонал
                        на производственной площадке.</p>
                    <h3>
                        Определите ответственных за обучение и сформируйте комиссии
                    </h3>
                    <p>
                        Назначьте не менее двух лиц, которые проводят обучение по охране труда при
                        работах на высоте. Второе лицо замещает основного ответственного во время его
                        отсутствия на рабочем месте (п. 96 Порядка № 2464). Информацию можно включить
                        в общий приказ о назначении ответственных за проведение обучения.
                    </p>
                    <p>
                        Определите работников, у которых есть опыт работы на высоте и понимание
                        процедуры изнутри, например мастера или бригадира участка. На обучении они смогут
                        показать на практике нюансы и специфику, которые известны только тем, кто реально
                        выполнял работы на высоте.
                    </p>
                    <p>
                        Дополнительную работу оформите совмещением. Для совмещения возьмите
                        согласие работников на новые обязанности и оформите дополнительное соглашение
                        к трудовому договору. Предусмотрите в нем оплату за такую работу (ст. 60.2, 151 ТК).
                    </p>
                    <p>
                        Сформируйте аттестационную комиссию для обучения безопасным методам
                        и приемам выполнения работ на высоте. В комиссию включите работников, которые
                        соответствуют четырем требованиям.
                    </p>
                    <ol>
                        <li>Прошли обучение в учебном центре по программам А, Б и В.</li>
                        <li>Имеют 3 группу по безопасности работ на высоте.</li>
                        <li>Старше 21 года.</li>
                        <li>Выполняют работу на высоте более двух лет.</li>
                    </ol>
                    <p>
                        Требования указаны в пункте 21 ПОТ № 782н, пунктах 74, 75 Порядка № 2464.
                        Как направить работников на обучение в учебный центр, читайте выше.
                    </p>
